package com.training.day10.operators;

public class RelationalOperator {
	public static void main(String[] args) {
		int X = 10;
		int Y = 20;
		System.out.println("(X == Y) = " + (X == Y));
		System.out.println("(X != Y) = " + (X != Y));
		System.out.println("(X > Y) = " + (X > Y));
		System.out.println("(X < Y) = " + (X < Y));
		System.out.println("(X >= Y) = " + (X >= Y));
		System.out.println("(X <= Y) = " + (X <= Y));
	}
}
